
public class Song {

	private String name;
	private String singer;
	private String path;
	
	public Song(String name, String singer, String path) {
		this.name = name;
		this.singer = singer;
		this.path = path;
	}
	
	public String getName() {
		return name;
	}
	
	public String getSinger() {
		return singer;
	}
	
	public String getPath() {
		return path;
	}
	
	
	
}
